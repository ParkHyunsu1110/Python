##터틀 그래픽 두번째 실습
import turtle as t
t.shape("turtle")

t.pencolor("yellow")
t.fillcolor("yellow")
t.begin_fill()
t.circle(70, 360, 7)
t.end_fill()

t.pencolor("blue")
t.fillcolor("blue")
t.begin_fill()
t.circle(60, 360, 6)
t.end_fill()

t.pencolor("red")
t.fillcolor("red")
t.begin_fill()
t.circle(50, 360, 5)
t.end_fill()

t.done()
